; COR24 assembly: prints "Hello, World!\n" via UART and halts.
;
; This is a Path-A fixture per
;   tools/briefs/dcftn-fortran-hello-world.md
; The full FTI-0 compiler isn't ready to emit this from
;   examples/hello.f
; yet; see docs/snobol4-blockers.md for the upstream gate. Until
; then, scripts/fortran short-circuits hello.f to this prebaked .s.
;
; Structure adapted from the tc24r demo
;   sw-cor24-x-tinyc/demos/hello.s
; (proven working under cor24-asm + cor24-emu). The only change is
; the byte sequence in _S0: "Hello, World!\n\0" instead of
; "Hello, COR24!\n\0".

.text

        .globl  _start
_start:
        la      r0,_main
        jal     r1,(r0)
_halt:
        bra     _halt

        .globl  _putc
_putc:
        push    fp
        push    r2
        push    r1
        mov     fp,sp
L1:
        la      r0,16711937
        lbu     r0,0(r0)
        push    r0
        la      r0,128
        mov     r1,r0
        pop     r0
        and     r0,r1
        ceq     r0,z
        brt     L2
        bra     L1
L2:
        lw      r0,9(fp)
        push    r0
        la      r0,16711936
        mov     r1,r0
        pop     r0
        sb      r0,0(r1)
L0:
        mov     sp,fp
        pop     r1
        pop     r2
        pop     fp
        jmp     (r1)

        .globl  _puts
_puts:
        push    fp
        push    r2
        push    r1
        mov     fp,sp
L4:
        lw      r0,9(fp)
        lbu     r0,0(r0)
        ceq     r0,z
        brt     L5
        lw      r0,9(fp)
        lbu     r0,0(r0)
        push    r0
        la      r0,_putc
        jal     r1,(r0)
        add     sp,3
        lw      r0,9(fp)
        push    r0
        lc      r0,1
        mov     r1,r0
        pop     r0
        add     r0,r1
        sw      r0,9(fp)
        bra     L4
L5:
L3:
        mov     sp,fp
        pop     r1
        pop     r2
        pop     fp
        jmp     (r1)

        .globl  _main
_main:
        push    fp
        push    r2
        push    r1
        mov     fp,sp
        la      r0,_S0
        push    r0
        la      r0,_puts
        jal     r1,(r0)
        add     sp,3
        lc      r0,0
        bra     L6
L6:
        mov     sp,fp
        pop     r1
        pop     r2
        pop     fp
        jmp     (r1)

        .data
_S0:
        ; "Hello, World!\n\0" -- 15 bytes
        .byte   72,101,108,108,111,44,32,87,111,114,108,100,33,10,0
